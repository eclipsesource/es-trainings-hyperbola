package org.eclipsercp.hyperbola.skype;

import org.eclipsercp.hyperbola.model.ContactsEntry;
import org.eclipsercp.hyperbola.model.ContactsGroup;
import org.eclipsercp.hyperbola.model.ISession;

public class SkypeSession implements ISession {

	public ContactsGroup getRoot() {
		ContactsGroup result = new ContactsGroup(null, "Skype");
		result.addEntry(new ContactsEntry(result, "bob@skype", "", "skype"));
		return result;
	}

}
